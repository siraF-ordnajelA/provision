		[Tipo de cliente],
		[Subtipo de cliente],
		[Programacion],
		[Fecha_de_actualizacion],
		[Fecha_de_cumplimiento_inst],
		[Asignacion],
		Averia,
		[Subtipo de Actividad Instalacion],
		[Código de actuación Instalacion],
		[Técnico que instaló],
		[Empresa que instaló],
		[Multiproducto instalado],
		[Tecnologia instalada],
		[Fecha creación TOA]
		
from av_averias
where [Access ID] = @access